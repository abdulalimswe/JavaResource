package org.example;

public class PrimitiveDataTypes {

    public static void main(String[] args) {
        int x = 10;
        long l = 100L;
        double y = 10.534543d;
        char ch = 'A';
        float f = 10.5f;

        System.out.println(x);
        System.out.println(l);
        System.out.println(y);
        System.out.println(ch);
        System.out.println(f);
    }
}
